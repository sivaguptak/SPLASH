  static List<ServiceProvider> searchProviders({
    String? query,
    String? location,
    double? radius,
  }) {
    List<ServiceProvider> results = List.from(_allProviders);

    // Filter by location
    if (location != null && location.isNotEmpty) {
      results = results.where((provider) => 
        provider.location.toLowerCase().contains(location.toLowerCase())
      ).toList();
    }

    // Filter by radius
    if (radius != null && radius > 0) {
      results = results.where((provider) => 
        provider.distance <= radius
      ).toList();
    }

    // Filter by search query
    if (query != null && query.isNotEmpty) {
      final lowerQuery = query.toLowerCase();
      results = results.where((provider) =>
        provider.name.toLowerCase().contains(lowerQuery) ||
        provider.category.toLowerCase().contains(lowerQuery) ||
        provider.subcategory.toLowerCase().contains(lowerQuery) ||
        provider.description.toLowerCase().contains(lowerQuery) ||
        provider.services.any((service) => service.toLowerCase().contains(lowerQuery))
      ).toList();
    }

    // Sort by distance
    results.sort((a, b) => a.distance.compareTo(b.distance));

    return results;
  }

